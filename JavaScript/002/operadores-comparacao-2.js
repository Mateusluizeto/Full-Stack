/* Operadores de comparação

    >       -> maior
    >=      -> maior ou igual
    <       -> menor
    <=      -> menor ou igual

*/

const firstNumber = 10
const seccondNumber = 20

console.log(firstNumber > seccondNumber) // false
console.log(firstNumber < seccondNumber) // true
console.log(firstNumber >= seccondNumber) // false
console.log(firstNumber <= seccondNumber) // true