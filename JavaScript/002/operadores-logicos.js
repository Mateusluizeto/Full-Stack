/* OPERADORES LÓGICOS

&& -> E
    true && true = true
    true && false = false
    false && false = false

||  -> Ou
    true || true = true
    true || false = true
    false || false = false

!   -> Negação
    !true = false
    !false = true
*/

console.log(true && false)
console.log(true || false || false || false)
console.log(!true)