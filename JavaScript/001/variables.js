/*
Declaração:

    var: escopo de função, pode ser redeclarada (legado, menos usado hoje)
    let: escopo de bloco, pode ser reatribuída mas não redeclarada
    const: escopo de bloco, valor não pode ser reatribuído (use para valores constantes)

Nomenclatura:

    Nomes descritivos e em camelCase (ex: minhaVariavel)
    Começa com letra, _ ou $
    Case-sensitive: nome é diferente de Nome

Tipos de dados:

    Primitivos: string, number, boolean, null, undefined, symbol, bigint
    Objetos: object, array, function

*/